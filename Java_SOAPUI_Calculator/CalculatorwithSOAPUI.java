package controller;

public class CalculatorDemo {
	private static double add(double NumberA, double NumberB)
	{
		return NumberA+NumberB;
	}
	private static double sub(double NumberA, double NumberB)
	{
		return NumberA-NumberB;
	}
	private static double mul(double NumberA, double NumberB)
	{
		return NumberA*NumberB;
	}
	private static double div(double NumberA, double NumberB)
	{
		return NumberA/NumberB;
	}
	

}
